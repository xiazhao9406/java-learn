package com.javalearn.charpter03;

public class Compare {
	public static void main(String[] args) {
		int number1 = 4;
		int number2 = 5;
		// the boolean result of < > == !=.
		System.out.println("number1 > number2: " + (number1 > number2));
		System.out.println("number1 < number2: " + (number1 < number2));
		System.out.println("number1 == number2: " + (number1 == number2));
		System.out.println("number1 != number2: " + (number1 != number2));
		System.out.println("number1 >= number2: " + (number1 >= number2));
		System.out.println("number1 <= number2: " + (number1 <= number2));
	}

}

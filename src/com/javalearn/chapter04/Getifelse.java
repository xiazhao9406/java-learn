package com.javalearn.chapter04;

public class Getifelse {
	public static void main(String[] args) {
		int math = 95;
		int english = 56;
		if (math > 60) {
			System.out.println("the math is passed");
		} else {
			System.out.print("the math is failed");
		}
		
		if (english > 60) {
			System.out.println("the english is passed");
		} else {
			System.out.println("the english is failed");
		}
	}

}
